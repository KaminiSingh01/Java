package Basic_program;
public class swap_two_num {
    public static void main(String[] main){
        int a=10;
        int b=56;
        System.out.println("Before Swapping..");
        System.out.println("a="+a+"\nb="+b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping..");
        System.out.println("a="+a+"\nb="+b);
    }
    
}
